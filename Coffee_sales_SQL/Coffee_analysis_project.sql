-- Coffee Analysis

/* 1. Coffee consumer
How many people in each city are estimated to consume coffee, 
given that 25% of the population does?
*/

with coffee_consumed(city,coffee_consumers,population) as
(
select cit.city_name,
	   count(cus.customer_name),
	   cit.population
from city cit
join customers cus
on cit.city_id = cus.city_id
group by cit.city_name, cit.population
)
select *, (coffee_consumers/population)*1000000 as percent
from coffee_consumed
order by coffee_consumers desc
;

select city_name,
	   round((population * 0.25)/1000000,2) as consumers_in_millions
from city
order by 2 desc
;

